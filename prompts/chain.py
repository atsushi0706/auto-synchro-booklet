"""冊子ライター(Gemini Flash, チェーンプロンプト)。

一撃生成はしない。表紙→①→②→③をセクションごとに別プロンプトで生成し、
各冊子は Haiku の意識プロファイルだけを入力に書く(裏ロジック非開示)。
"""

from __future__ import annotations

import json

from .llm import gemini
from .worldview import (
    WORLDVIEW, TONE, banned_terms_clause,
    SUPERVISOR_NAME, PRODUCT_NAME, SESSION_NAME,
)

_SYS = (
    WORLDVIEW + "\n" + TONE + "\n" + banned_terms_clause()
    + "\n出力は読みやすい日本語の本文のみ。見出しは # や ## の Markdown で。"
    "箇条書きは使いすぎない。一冊の手紙のように、その人だけに向けて書く。"
    "\n【厳守】意識プロファイルに書かれた“その人固有の特徴”だけを根拠に"
    "書く。プロファイルに無い人物像(自己犠牲・本音を飲み込む等)を"
    "勝手に足さない。プロファイルの『意識の核』『意識の軸』『お金との"
    "関わり方のクセ』をそのまま具体的に反映し、別人なら全く違う文章に"
    "なるように、固有名詞のように特徴を効かせること。"
)


def _p(profile: dict) -> str:
    return json.dumps(profile, ensure_ascii=False, indent=2)


# 表紙は固定デザイン(data/cover.svg)。文章生成はしない
# (誰にでも使える固定表紙・APIコスト削減)。タイトルと氏名は
# アプリ側で画像の上に重ねる。


def gen_book1(name: str, profile: dict) -> str:
    prompt = f"""{name} さん専用「① あなた専用の取扱説明書」本編を書きます。
意識プロファイル(この人固有・唯一の根拠):
{_p(profile)}

次の構成で、{name} さんに語りかける一冊として執筆(各 220〜320字):
## あなたという人の核（『意識の核』『強み』を、その人固有の言葉で）
## あなたの意識のクセ（どこへ飛びやすく、どう戻ると楽か）
## あなたのシンクロ率（『シンクロ率の傾向』を踏まえ、強い/繊細など
   そのタイプ固有の起き方。全員同じにしない）
## シンクロが起きやすい場面
## あなたの中に眠っている『過去の契約』の傾向
## 自分に戻るための、あなた専用の手順（軸に合った具体策）
末尾に、名前のエネルギーがこの傾向をどう後押ししているかを 1 段落。"""
    return gemini(_SYS, prompt, max_tokens=4000)


def gen_book2(name: str, profile: dict) -> str:
    prompt = f"""{name} さん専用「② あなたに合ったパートナー説明書」本編。
意識プロファイル:
{_p(profile)}

人間関係の地図として、プロファイルの『飛びやすい相手』
『自分を保てる相手』『関わり方の処方箋』『意識の軸』を根拠に、
次の構成で執筆(各 240〜340字):
## あなたの意識が飛びやすい相手のタイプ
## あなたが自分を保てる相手のタイプ
## 関わり方の処方箋（その人の軸に合った具体的な距離の取り方）
断定で人を選別しない。相手を悪者にしない。
この人の軸が自分軸なら「合わせすぎる人」前提で書かない。"""
    return gemini(_SYS, prompt, max_tokens=4000)


def gen_book3(name: str, profile: dict) -> str:
    prompt = f"""{name} さん専用「③ あなたに合ったお金の引き寄せ説明書」本編。
意識プロファイル:
{_p(profile)}

「意識のポジションが自分に戻ると、お金の流れも変わる」前提で、
プロファイルの『お金との関わり方のクセ』『意識が戻ると変わること』
『引き寄せの整え方』『意識の軸』を唯一の根拠に、次の構成で執筆
(各 240〜340字):
## あなたとお金の、これまでの関わり方のクセ
   ※プロファイルの『お金との関わり方のクセ』をそのまま土台にする。
   自分軸の人に「誰かのために稼いできた」と書くのは禁止。
   自分の世界・自分の納得で動くタイプならその通り書く。
## 意識が戻ったときに変わるお金の流れ
## あなたのタイプに合った、お金との関わり方・引き寄せの整え方
   （『引き寄せの整え方』を具体的な日々の手順に落とす）
投資勧誘・金額の保証・「必ず儲かる」等は書かない。誠実に。"""
    return gemini(_SYS, prompt, max_tokens=4000)


def generate_booklet(name: str, profile: dict) -> dict:
    """3冊をチェーンで生成して返す(表紙は固定デザインのため非生成)。"""
    return {
        "book1": gen_book1(name, profile),
        "book2": gen_book2(name, profile),
        "book3": gen_book3(name, profile),
    }
